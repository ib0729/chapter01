package com.javaex.ex04;

public class Ex06 {

	public static void main(String[] args) {
		

		for(int y=0; y<6; y++) {
			for(int x=0; x<6; x++) {        //y=1 , x->6개
				System.out.print("*");		//y=2 , x->6개
			}								//y=3 , x->6개
			System.out.println();			//y=4 , x->6개
		} // for문							//y=5 , x->6개
		System.out.println();				//y=7 , x->6개
	}

}
